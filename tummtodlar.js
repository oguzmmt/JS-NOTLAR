/* JavaScript'de çok sayıda farklı yerleşik metod (method) bulunur. Bu metodlar, farklı işlevlere sahiptir ve JavaScript programlarını yazarken oldukça işe yararlar. 
İşte bazı yaygın JavaScript metodları:

1. **String Metodları:** Dize (string) verileri üzerinde çalışmak için kullanılır. Örnekler: `length()`, `charAt()`, `concat()`, `toUpperCase()`, `toLowerCase()`.

2. **Array Metodları:** Dizi (array) verileri üzerinde çalışmak için kullanılır. Örnekler: `push()`, `pop()`, `shift()`, `unshift()`, `splice()`, `slice()`, `map()`, `filter()`, `reduce()`.

3. **Object Metodları:** Nesne (object) verileri üzerinde çalışmak için kullanılır. Örnekler: `hasOwnProperty()`, `keys()`, `values()`.

4. **Math Metodları:** Matematiksel işlemler için kullanılır. Örnekler: `Math.round()`, `Math.floor()`, `Math.ceil()`, `Math.min()`, `Math.max()`.

5. **Date Metodları:** Tarih ve saat işlemleri için kullanılır. Örnekler: `getYear()`, `getMonth()`, `getDate()`, `getHours()`, `getMinutes()`, `toLocaleDateString()`.

6. **Regular Expression Metodları:** Düzenli ifadelerle çalışmak için kullanılır. Örnekler: `match()`, `test()`, `exec()`, `replace()`, `split()`.

7. **Global Metodlar:** JavaScript diline özgü global metodlar. Örnekler: `parseInt()`, `parseFloat()`, `isNaN()`, `isNaN()`.

8. **DOM Metodları:** Web sayfası içeriği üzerinde çalışmak için kullanılır. Örnekler: `getElementById()`, `querySelector()`, `appendChild()`, `removeChild()`.

9. **JSON Metodları:** JSON veri formatı ile çalışmak için kullanılır. Örnekler: `JSON.parse()`, `JSON.stringify()`.

10. **Promise Metodları:** Asenkron işlemler ve promise nesneleriyle çalışmak için kullanılır. Örnekler: `then()`, `catch()`, `all()`, `race()`.

11. **Set Metodları:** Set veri yapısı ile çalışmak için kullanılır. Örnekler: `add()`, `delete()`, `has()`, `clear()`.

12. **Map Metodları:** Map veri yapısı ile çalışmak için kullanılır. Örnekler: `set()`, `get()`, `delete()`, `has()`, `clear()`.

Bu sadece temel JavaScript metodlarına birkaç örnek içeriyor. JavaScript geliştikçe ve yeni özellikler ekledikçe metodlar ve işlevler de çeşitlenmektedir. 
İhtiyacınıza göre bu metodları kullanarak JavaScript projelerinizi daha verimli bir şekilde geliştirebilirsiniz. 
*/

